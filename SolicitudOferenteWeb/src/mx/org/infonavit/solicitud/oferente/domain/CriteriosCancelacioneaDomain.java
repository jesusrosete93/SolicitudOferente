		domain.setFechaCancelacionInicio(forma.getFechaCancelacionInicio());
		domain.setFechaCancelacionFin(forma.getFechaCancelacionFin());
		domain.setFechaSolicitudInicio(forma.getFechaSolicitudInicio());
		domain.setFechaSolicitudFin(forma.getFechaSolicitudFin());
